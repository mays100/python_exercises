# עבודה עם רשימות
# תאריך: 26-01-2025

# 1. יצירת רשימה עם 10 מספרים
my_list = [4, 7, 2, 9, 5, 1, 8, 3, 6, 10]
print("הרשימה ההתחלתית:", my_list)

# 2. שימוש במתודות רשימה
# 2.1 הוספת המספר 11 לסוף הרשימה
my_list.append(11)
print("הרשימה לאחר הוספת 11:", my_list)

# 2.2 הסרת המספר 5 מהרשימה (אם קיים)
if 5 in my_list:
    my_list.remove(5)
    print("הרשימה לאחר הסרת 5:", my_list)
else:
    print("המספר 5 לא נמצא ברשימה.")

# 2.3 הוספת המספר 3 באינדקס 2 ברשימה
my_list.insert(2, 3)
print("הרשימה לאחר הוספת 3 באינדקס 2:", my_list)

# 3. פעולות על הרשימה המעודכנת
# 3.1 חישוב והדפסת סכום כל המספרים ברשימה
sum_of_list = sum(my_list)
print("סכום כל המספרים ברשימה:", sum_of_list)

# 3.2 מציאת והדפסת המספר הקטן והגדול ביותר ברשימה
smallest_number = min(my_list)
largest_number = max(my_list)
print("המספר הקטן ביותר ברשימה:", smallest_number)
print("המספר הגדול ביותר ברשימה:", largest_number)