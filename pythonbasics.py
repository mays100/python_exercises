# תרגיל 1: שימוש בפונקציות len(), input(), ו-print()
# 1. בקש מהמשתמש להזין משפט
sentence = input("בבקשה הזן משפט: ")

# 2. השתמש בפונקציה len() כדי לחשב את אורך המשפט
sentence_length = len(sentence)

# 3. הדפס הודעה שאומרת למשתמש את אורך המשפט שלו
print(f"אורך המשפט שהזנת הוא: {sentence_length} תווים.")

# תרגיל 2: מתן שמות למשתנים
# משתנה שיאחסן את הצבע האהוב עליך
favorite_color = "כחול"

# משתנה שיאחסן את הגיל שלך
age = 30

# משתנה שיאחסן את שם החיה האהובה עליך
favorite_animal_name = "חתול"
# הסבר מדוע השמות תקפים ומתאימים:
#
# favorite_color:
# תקף: מכיל אותיות קטנות, קו תחתון (_) שמפריד בין מילים (מקובל בפייתון לשמות משתנים מרובי מילים), ואין בו רווחים או תווים מיוחדים אחרים.
# מתאים: השם ברור ומתאר במדויק את הערך שהמשתנה אמור להכיל - הצבע האהוב.
#
# age:
# תקף: מכיל אותיות קטנות בלבד.
# מתאים: השם קצר, ברור ומייצג את הגיל.
#
# favorite_animal_name:
# תקף: מכיל אותיות קטנות וקו תחתון להפרדה בין מילים.
# מתאים: השם מתאר בבירור את הערך שהמשתנה אמור להכיל - שם החיה האהובה. השימוש ב-name בסוף מוסיף בהירות שאנחנו מתכוונים לשם ולא לחיה עצמה (למרות שבמקרה הזה זה די ברור).
print(f"הצבע האהוב עלי הוא: {favorite_color}")
print(f"הגיל שלי הוא: {age}")
print(f"החיה האהובה עלי היא: {favorite_animal_name}")